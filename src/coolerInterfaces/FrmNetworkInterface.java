    private void btnShowTemperatureMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnShowTemperatureMouseClicked
        temperatureSensorManager = TemperatureSensorManager.createAsSingleton();
        coolerManager = CoolerManager.createAsSingleton();
        temperatureSensor.setMeasuredValue(temperatureSensorManager.measureTemperature());
        temperatureSensor.setIsActive("Yes");
        temperatureSensorManager.update(temperatureSensor);
        cooler.setInstantWorkingStatus("Temperature Measurement");
        coolerManager.updateInstantWorkingStatus(cooler);
        lblInstantStatus.setText(cooler.getInstantWorkingStatus());
        pnlInstantStatus.setBackground(new Color(168, 95, 0));
        lblUserInfo.setText(" ");
        try {
            TimeUnit.SECONDS.sleep(2);
        } catch (InterruptedException ex) {
            Logger.getLogger(FrmNetworkInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        DecimalFormat decimalFormat = new DecimalFormat();
        decimalFormat.setMaximumFractionDigits(4);
        
        pnlTemperatureValue.setBackground(new Color(168, 95, 0));
        lblTemperatureLogo.setText("Temperature:");
        lblTemperatureValue.setText(String.valueOf(decimalFormat.format(temperatureSensor.getMeasuredValue())) + " Degrees Celsius");
        
        cooler.setInstantWorkingStatus("Open");
        lblInstantStatus.setText(cooler.getInstantWorkingStatus());
        coolerManager.updateInstantWorkingStatus(cooler);
        pnlInstantStatus.setBackground(new Color(0, 153, 41));
        pnlStatusIndicator.setBackground(new Color(0, 153, 41));           
    }//GEN-LAST:event_btnShowTemperatureMouseClicked
    
