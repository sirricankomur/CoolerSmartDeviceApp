        coolerManager.updateInstantWorkingStatus(cooler);
        
        lblInstantStatus.setText(cooler.getInstantWorkingStatus());
        pnlInstantStatus.setBackground(new Color(0, 153, 41));
        pnlStatusIndicator.setBackground(new Color(0, 153, 41));
